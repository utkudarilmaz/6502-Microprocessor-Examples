	.DATA $003F
	.BYTE 05,$75,$6D,$E0,$10,$24,$00
	
	.CODE $0200
	
	LDX #%00000001
	STX $46
	LDX $3F
	LDY #$8
	
BASLA	
	LDA $3F,X	
	AND $46
	BEQ GEC
	JMP KAYDIR

GEC	INC $45
	JMP KAYDIR

KAYDIR	LDA $46
	CLC
	ROL
	STA $46
	DEY
	BEQ PAS
	JMP BASLA

PAS	LDY #%00000001
	STY $46
	LDY #$8
	DEX
	BEQ BITIR
	JMP BASLA

BITIR	BRK
	END	